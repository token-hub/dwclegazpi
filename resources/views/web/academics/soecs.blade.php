	<h3>Engineering Programs</h3>
	<p>The engineering programs (civil and electrical engineering) aim to prepare the graduates for a professional engineering career in infrastructure, planning, and development in the built environment. The graduates must not only have the basic foundation of the civil engineering or electrical engineering profession but they should also be responsive to the economic, social and environmental demands of their work.</p>

	<h3>Specific Objectives:</h3>
	<ol>
		<li>Develop engineers who possess basic knowledge and skills in the planning design, construction, management and operation, and maintenance of engineering works;</li>
		<li>Develop engineers who take into account the effects to the environment of the infrastructure they will construct and the electrical system they will set up;</li>
		<li>Develop engineers who are fully aware of their legal and ethical responsibilities;</li>
		<li>Develop engineers who are knowledgeable in human, financial, physical energy and resource management; and</li>
		<li>Develop engineers who are fully oriented in the arts, science, technology, and engineering concepts.</li>
	</ol>

	<h3>Computer Studies Programs</h3>
	<p>The computer studies programs aim to produce globally competitive information technology (IT) professionals and socially responsible technopreneurs guided by the Divinian core values. The computer studies programs prepare students to become IT professionals and researchers who are proficient in designing and developing computing solutions, well-versed in application installation, operation, development, maintenance and administration, and familiar with hardware installation, operation, and maintenance.</p>

	<h3>Specific Objectives <br> Develop IT professionals who can: </h3>
	<ol>
		<li>Apply mathematical concepts, algorithmic principles, and computer science theory in the modeling and design of ICT solutions;</li>
		<li>Use analytical skills and appreciate the dynamic and collaborative nature of problem solving;</li>
		<li>Handle moral, legal, and social responsibilities;</li>
		<li>Recognize the need for continuing professional development and lifelong learning;</li>
		<li>Engage in technopreneurial activities utilizing appropriate tools in the field of information technology and who are globally competitive technopreneurs;</li>
		<li>Respond to innovations and diverse perspectives by using a wide range of ideas and techniques that will demonstrate originality and creativity in application development;</li>
		<li>Increase accessibility of ICT resources that will promote efficient and effective communication to various clienteles;</li>
		<li>Develop appropriate skills that are essential for independent learning in different areas of programming, networking, database management and troubleshooting;</li>
		<li>Apply known methods and skills, and learn new approaches based on current trends in order to develop applications that are ready for deployment;</li>
		<li>Understand the real world limits in order to evaluate and select new information resources and technological innovations based on the appropriate task that will demonstrate originality and creativity in application development.</li>
	</ol>

