	<p>The School of Hospitality Management (SOHM) through its three sections, namely: Hotel and Restaurant Management, Culinary Arts, and Travel and Tourism, commits itself to the Vision, Mission, and Goal of Divine Word College of Legazpi through the holistic development of its students for a wide range of professional positions in the hospitality industry.  It seeks to provide hands-on experiences emphasizing the acquisition of proficiency in service management, tourism management, and culinary skills.</p>

	<h3>Specific Objectives:</h3>
	<ol>
		<li>Develop a customer service orientation guided by principles of servant leadership;</li>
		<li>Develop the students’ ability to determine and respond to guest preferences and industry trends;</li>
		<li>Instill in the students a commitment to meeting safety and sanitation standards in the delivery of services;</li>
		<li>Produce graduates who can plan, carry out, and evaluate various hospitality menus and events, using effective control systems and technologies, and guided by business ethics and moral values;</li>
		<li>Stimulate the students to establish and maintain professional attitudes and appearance standards as service providers who go the extra mile;</li>
		<li>Promote social awareness and expanded social involvement among the faculty, students, and personnel to enhance the community members’ knowledge and skills in the preparation of a balanced and nutritious diet;</li>
		<li>Strengthen the research capacity and interest of faculty and students to generate new knowledge in the various fields of hospitality management;</li>
		<li>Establish strong linkages and networks with HM related organizations, government and private agencies, and industry partners;</li>
		<li>Introduce, as a way of life, the Divinian spirituality, and core values by integrating it into the curriculum and all activities of the HM Department; and</li>
		<li>Produce highly competitive graduates able to handle state-of-the-art facilities and equipment, including updated Information Technology support systems and services.</li>
	</ol>
	
