	<p>The DWCL Community Extension Service was created to serve the marginalized sector of the society through building Basic Ecclesial Communities (BEC’s) and educating the DWCL personnel and students on Christian – oriented service. The DWCL CES programs are designed to cater to communities that have high needs for basic services. Those programs are geared toward holistic and sustainable developments of BEC’s. In addition, the DWCL CES aims to kindle the spirit of genuine service among DWCL personnel and students, for they are the force that will ensure the attainment of the office’s objectives. They are, therefore provided direct exposures to the adopted communities.</p>

	<p>Through the Community Extension Service, the DWCL can make its program responsive to the needs of the people in the region that answers to the call and challenge for national developments.
	</p>

	<h3> CES OBJECTIVES</h3>

	<ol>
		<li>To involve all sectors of the academe in responding to the needs of the outside community;</li>
		<li>To raise clientele consciousness and develop their skills and competence in their field of specializations along socio-economic, political & ecological/environmental aspects;</li>
		<li>To provide and facilitate Livelihood Training and projects to the adopted and served communities;</li>
		<li>To provide and facilitate the delivery of basic social services to the adopted and other served communities.</li>
		<li>To undertake projects/activities for ecological and environmental preservation and protection.</li>
		<li>To establish linkages/networks with other institutions for a more effective community extension service.</li>
		<li>To provide and facilitate Christian Life Formation Activities</li>
	</ol>
	
