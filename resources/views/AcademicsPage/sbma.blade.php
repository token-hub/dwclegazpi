	<p>The School of Business Management and Accountancy (SBMA) is committed to excellence and dynamism in teaching-learning-research processes across all areas of discipline in business and public. It seeks to produce socially responsible and service-oriented graduates, imbued with the DWCL core values.</p>
	
	<h3>Specific Objectives:</h3>
	<ol>
		<li>Provide students with relevant and dynamic curricula in accountancy, management accountancy, business administration, entrepreneurship, and office administration;</li>
		<li>Enhance the students’ personality and academic preparation to strengthen their knowledge and skills in the various areas of business towards global competitiveness;</li>
		<li>Develop the research potentials of the students as a way of contributing to the existing knowledge and practices;</li>
		<li>Strengthen students’ well-being through a rewarding vocation as young business professionals;</li>
		<li>Provide the business community with competent, innovative, proficient, multi-skilled, and socially-responsive graduates; and</li>
		<li>Establish partnerships with the industry and other institutions in relevant areas such as, but not limited to, instruction, research, extension services, and on the job training, as a way to enhance the students’ knowledge skills, and personality.</li>
	</ol>

