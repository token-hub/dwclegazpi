		<li>Develop the child’s intellectual potentials by emphasizing critical thinking and sustaining the inculcation of values, integrated in all learning areas;</li>
		<li>Awaken the child’s artistic talents and creative abilities through various co-curricular offerings and activities;</li>
		<li>Promote religious education and character formation through the observance of various liturgical activities inside and outside the school, and awareness of the needs of his/her fellowmen especially the less fortunate;</li>
		<li>Deepen the child’s appreciation of his/her rich Filipino culture and provide him/her ways and avenues to uphold his/her rights and duties as a productive citizen in the society; and</li>
